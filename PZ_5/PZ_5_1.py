#Составить функцию решения задачи: из заданного числа вычли сумму его цифр. Из результата вновь вычли сумму его цифр и т. д. Через сколько таких действий получится нуль?

#Переменная
number = int(input("Введите число: "))

#Вычисляем сумму цифр числа `n'.
def sum_of_digits(n):
    """Возвращает сумму цифр числа n."""
    return sum(int(digit) for digit in str(n))

#Выполняем цикл, в котором из числа `n' вычитается сумма его цифр.
def steps_to_zero(n):
    """Возвращает количество шагов, необходимых для достижения нуля."""
    steps = 0
    while n > 0:
        n -= sum_of_digits(n)
        steps += 1
    return steps

#Вывод результата.
result = steps_to_zero(number)
print(f"Количество шагов для достижения нуля: {result}")
