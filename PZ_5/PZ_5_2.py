#Описать функцию PowerA234(параметры), вычисляющую вторую, третью и четвертую степень числа A и возвращающую эти степени соответственно в переменные B, C и D. С помощью этой функции найти вторую, третью и четвертую степень пяти данных чисел.

# Определяем функцию PowerA234, которая принимает один параметр A
def PowerA234(A):
    # Вычисляем степени
    B = A ** 2  # Вторая степень
    C = A ** 3  # Третья степень
    D = A ** 4  # Четвертая степень
    
    # Возвращаем результаты в виде кортежа
    return B, C, D

# Список чисел, для которых мы будем вычислять степени
numbers = [2, 3, 4, 5, 6]

# Проходим по каждому числу в списке
for number in numbers:
    # Вызываем функцию PowerA234 и получаем степени
    second, third, fourth = PowerA234(number)
    
    # Выводим результаты
    print(f"Число: {number}, Вторая степень: {second}, Третья степень: {third}, Четвертая степень: {fourth}")
