#Даны два целых числа A и B (A < B). Вывести в порядке возрастания все целые числа, расположенные между A и B (включая сами числа A и B), а также количество N этих чисел

#Вводим числа A и B
A = int(input("Введите число A (A < B): "))
B = int(input("Введите число B (A < B): "))

#Проверяем, что A меньше B
if A >= B:
    print("Ошибка: A должно быть меньше B.")
else:
    #Генерируем список чисел от A до B включительно
    numbers = list(range(A, B + 1))
    
    #Выводим числа
    print("Числа от A до B:", numbers)
    
    #Выводим количество чисел
    N = len(numbers)
    print("Количество чисел N:", N)