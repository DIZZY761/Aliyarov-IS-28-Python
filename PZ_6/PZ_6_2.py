#Дан список размера N. Найти номера тех элементов списка, которые больше своего правого соседа, и количество таких элементов. Найденные номера выводить в порядке их возрастания.

def find_greater_than_right_neighbor(lst):
    # Список для хранения индексов элементов, которые больше своего правого соседа
    indices = []
    
    # Проходим по списку, исключая последний элемент
    for i in range(len(lst) - 1):
        if lst[i] > lst[i + 1]:
            indices.append(i)
    
    # Выводим количество таких элементов и их индексы
    print("Количество элементов, которые больше своего правого соседа:", len(indices))
    print("Индексы таких элементов:", indices)

#Переменные
N = int(input("Введите размер списка N: "))
lst = []

# Заполнение списка
for i in range(N):
    element = int(input(f"Введите элемент {i + 1}: "))
    lst.append(element)

find_greater_than_right_neighbor(lst)
    
