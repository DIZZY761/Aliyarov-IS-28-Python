#Даны положительные числа A, B, C. На прямоугольнике размера A х B размещено максимально возможное количество квадратов со стороной C (без наложений). Найти количество квадратов, размещенных на прямоугольнике. Операции умножения и деления не использовать.

#Переменные
A  = int(input("Ширина прямоугольника: "))
B = int(input("Высота прямоугольника: "))
C = int(input("Сторона квадрата: "))

#Функция для нахождения количества квадратов
def count_squares(A, B, C):
    #Инициализируем счетчики для количества квадратов по ширине и высоте
    count_width = 0
    count_height = 0
    
    #Считаем сколько квадратов помещается по ширине
    while A >= C:
        A -= C  #Уменьшаем ширину на размер квадрата
        count_width += 1  #Увеличиваем счетчик квадратов по ширине
    
    #Считаем сколько квадратов помещается по высоте
    while B >= C:
        B -= C  #Уменьшаем высоту на размер квадрата
        count_height += 1  #Увеличиваем счетчик квадратов по высоте
    
    #Общее количество квадратов равно сумме квадратов по ширине и высоте
    total_squares = 0
    for _ in range(count_height):  #Для каждого квадрата по высоте
        total_squares += count_width  #Добавляем количество квадратов по ширине
    
    return total_squares #Возвращаем общее количество квадратов

# Выводим результат
result = count_squares(A, B, C)
print("Количество квадратов, размещенных на прямоугольнике: ", result)
